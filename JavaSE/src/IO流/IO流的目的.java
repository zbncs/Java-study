package IO流;

public class IO流的目的 {
    /**
     * 目的：
     * 1，将数据写到文件中，实现数据永久化存储
     * 2，把文件中的数据读取到内存中(Java程序)
     *
     * IO的数据传输，可以看做是一种数据的流动，按照流动的方向，以内存为参照物，进行读写操作。
     * 简单来说：内存在读，内存在写。
     *
     *    I  表示intput  ，是数据从硬盘进内存的过程，称之为读数据。
     *    O 表示output ，是数据从内存到硬盘的过程。称之为写数据。
     *
     *    IO流分类：
     *          按照流向分类：输入流和输出流；
     *          按照类型区分：字节流 和 字符流；
     *          字节流可以操作所有类型文件；字符流只能操作纯文本；
     *
     * */
}
